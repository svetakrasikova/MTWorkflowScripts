my $lineCount = 0;
my $base = new IO::Uncompress::Bunzip2 $baseLine
or die encode "utf-8", "Cannot open baseline file “$baseLine”: $Bunzip2Error\n";
while (my $line = decode "utf-8", $base->getline()) {
	++$lineCount;
	unless ($lineCount % 100000) {
		print ".";
		print "[base: $lineCount]" unless $lineCount % 1000000;
	}
	chomp $line;
	my ($source, $target, $tm) = split //, $line;
	die "Could not find $tm!\n" unless defined $preFlightMap{$tm} || $tm =~ /_doc$/;
	$tm = $preFlightMap{$tm} unless $tm =~ /_doc$/;
	++$corpus{$tm}->{$source}->{$target} unless $tm eq "delete";
}
close $base;
print encode "utf-8", "Baseline line count: $lineCount\n";

my $multiplyAmbiguousSources = my $updatedTranslations = 0;
my %updatedSources;

$lineCount = 0;
my $curr = new IO::Uncompress::Bunzip2 $current
or die encode "utf-8", "Cannot open current file “$current”: $Bunzip2Error\n";
while (my $line = decode "utf-8", $curr->getline()) {
	++$lineCount;
	unless ($lineCount % 100000) {
		print encode "utf-8", "·";
		print "[current: $lineCount]" unless $lineCount % 1000000;
	}
	chomp $line;
	my ($source, $target, $tm) = split //, $line;
	die "Could not find $tm!\n" unless defined $newProductMap{$tm} || $tm =~ /_doc$/;
	$tm = $newProductMap{$tm} unless $tm =~ /_doc$/;
	next if $tm eq "delete";
	if (defined $corpus{$tm} && defined $corpus{$tm}->{$source} && !defined $corpus{$tm}->{$source}->{$target}) {
		if (1<(()=keys %{$corpus{$tm}->{$source}})) {
			++$multiplyAmbiguousSources;
#			print encode "utf-8", "Ambiguous source in TM “$tm”:\n\tSource: $source\n";
#			print encode "utf-8", "\tBaseline:\n\t\t".(join "\n\t\t", keys %{$corpus{$tm}->{$source}})."\n";
#			print encode "utf-8", "\tCurrent:\n\t\t$target\n";
#			scalar <STDIN>;
		} else {
			++$updatedTranslations;
			$corpus{$tm}->{$source} = {} unless defined $updatedSources{$tm} && defined $updatedSources{$tm}->{$source};
			++$updatedSources{$tm}->{$source};
		}
	}
	++$corpus{$tm}->{$source}->{$target} unless defined $corpus{$tm}->{$source}->{$target};
}
close $curr;
print encode "utf-8", "Current line count: $lineCount\n";

$lineCount = 0;
my $out = new IO::Compress::Bzip2 $output
or die encode "utf-8", "Cannot write output file “$output”: $Bzip2Error\n";
foreach my $tm (sort {$a cmp $b} keys %corpus) {
	foreach my $source (sort {$a cmp $b} keys %{$corpus{$tm}}) {
		foreach my $target (sort {$a cmp $b} keys %{$corpus{$tm}->{$source}}) {
			$lineCount += $corpus{$tm}->{$source}->{$target};
			unless ($lineCount % 100000) {
				print encode "utf-8", "˙";
				print "[output: $lineCount]" unless $lineCount % 1000000;
			}
			print $out encode "utf-8", "$source$target$tm◊÷\n" foreach 1..$corpus{$tm}->{$source}->{$target};
		}
	}
}
close $out;
print encode "utf-8", "Output line count: $lineCount\n";

